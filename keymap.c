bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case ROTARY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(rotary_position);
      }
      return false;
      break;
    case LSFT(ROTARY):
      if (record->event.pressed) {
        rotary_position = 0;
        set_single_persistent_default_layer(rotary_position);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool anticlockwise) {
    if (index == 0) {
        if (anticlockwise) {
            if(rotary_position == 0) { rotary_position = 20; }
            rotary_position--;
            tap_code(KC_AUDIO_VOL_DOWN);
        } else {
            if(rotary_position == 19) { rotary_position = 0; }
            rotary_position++;
            tap_code(KC_AUDIO_VOL_UP);
        }
        uprintf("Rotary possition: %d\n", rotary_position);
    }
}
